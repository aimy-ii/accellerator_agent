def format_dialog(messages: list[dict]) -> str:
    """Собирает историю диалога в текст для LLM."""
    lines: list[str] = []
    for m in messages:
        role = "Заказчик" if m.get("role") == "user" else "Ассистент"
        content = (m.get("content") or "").strip()
        if content:
            lines.append(f"{role}: {content}")
    return "\n".join(lines) if lines else "— (диалога ещё нет)"


def questions_user_message(
    messages: list[dict],
    *,
    spec_context: str | None = None,
) -> str:
    """Формирует user-сообщение для генерации вопросов.

    Args:
        messages: история диалога.
        spec_context: текст существующего ТЗ (режим доработки).
            Заказчик его в чате НЕ видит — это скрытый контекст.
    """
    parts = [f"История диалога:\n{format_dialog(messages)}"]

    if spec_context:
        parts.append(
            "\nСКРЫТЫЙ КОНТЕКСТ — действующее ТЗ этого проекта. Заказчик его в чате не видит.\n"
            "Веди себя так, будто ты уже знаком с проектом. НЕ пересказывай ТЗ, НЕ цитируй его.\n"
            "Спрашивай только про ИЗМЕНЕНИЯ: что добавить, убрать, переделать.\n"
            f"---\n{spec_context[:12000]}\n---"
        )

    return "\n".join(parts)


def assess_user_message(messages: list[dict], *, spec_context: str | None = None) -> str:
    """Формирует user-сообщение для оценки достаточности информации."""
    parts = [f"История диалога:\n{format_dialog(messages)}"]
    if spec_context:
        parts.append(
            "\nУ проекта уже есть действующее ТЗ (скрытый контекст), "
            "поэтому базовая информация о продукте считается известной.\n"
            f"---\n{spec_context[:8000]}\n---"
        )
    return "\n".join(parts)
