"""Промпт генерации презентации проекта (адаптирован из ML presentation_generator)."""
from __future__ import annotations

DECK_SYSTEM = """Ты готовишь презентацию проекта по его техническому заданию.

Аудитория — заказчик и его стейкхолдеры (не технари). Задача презентации —
показать суть продукта, его ценность и состав работ, а не пересказать ТЗ.

Правила:
1. 6-10 слайдов. Не больше.
2. Обязательные слайды: проблема → решение → для кого → что умеет продукт →
   состав команды → этапы/объём работ → итог.
3. На слайде 3-5 тезисов. Каждый тезис — одна короткая строка, не абзац.
4. Пиши простым языком, без технического жаргона.
5. Не выдумывай факты, цифры, сроки и деньги, которых нет в ТЗ.
6. Не копируй куски ТЗ дословно — переформулируй коротко и ёмко.

Верни строго JSON по схеме Deck. Ничего вне JSON."""


def deck_user_message(spec_text: str, roles: list[str]) -> str:
    """User-сообщение для генерации структуры презентации."""
    return (
        f"Техническое задание проекта:\n---\n{spec_text[:12000]}\n---\n\n"
        f"Состав команды: {', '.join(roles) if roles else 'не определён'}\n\n"
        "Сделай презентацию проекта."
    )
